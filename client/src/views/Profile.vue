<template>
    <div class="profile">
    <div class="container">
        <h2>Account Settings</h2>
        <div class="profile-info">
          <router-view v-bind:user="user" />
          <div class="input">
            <label for="username">User Name:</label>
            <input disabled type="text" id="username" v-model="username" />
          </div>
          <div class="input">
            <label for="firstName">First Name:</label>
            <input type="text" id="firstName" v-model="firstName" />
          </div>
          <div class="input">
            <label for="lastName">Last Name:</label>
            <input type="text" id="lastName" v-model="lastName" />
          </div>
          <div class="input">
            <label for="email">Email  :    </label>
            <input disabled type="text" id="email" v-model="email" />
          </div>
          <div class="input">
            <label for="password">Password:</label>
            <input type="text" id="password" v-model="password" />
          </div>
          <div class="input">
            <label for="university">Enrolled at: </label>
            <input disabled type="text" id="university" v-model="university" />
          </div>
          <div class="input">
            <label for="campus">Campus: </label>
            <input type="text" id="campus" v-model="campus" />
          </div>
          <button @click="updateProfile">Save Changes</button>
          <button @click="deleteAccount">Delete Account</button>
        </div>
      </div>
    </div>
  </template>
<script>

import { Api } from '@/Api'

export default {
  name: 'Profile',
  props: {
    user: Object
  },
  data() {
    return {
      username: '',
      firstName: '',
      lastName: '',
      email: '',
      password: '',
      university: '',
      campus: '',
      id: ''
    }
  },
  // if user is not authorized return to login page
  created() {
    if (localStorage.getItem('token') === null) {
      this.$router.push('/login')
    }
  },
  mounted() {
    Api.get('/user', { headers: { token: localStorage.getItem('token') } })
      .then(res => {
        this.username = res.data.user.username
        this.firstName = res.data.user.firstName
        this.lastName = res.data.user.lastName
        this.email = res.data.user.email
        this.password = res.data.user.password
        this.campus = res.data.user.campus
        this.university = res.data.user.university
        this.id = res.data.user.id
      })
  },
  methods: {
    logout() {
      localStorage.clear()
      this.$router.push('/login')
    },
    deleteAccount() {
      Api.delete(`/users/${this.user.id}`)
        .then((res) => {
          localStorage.clear()
          console.log(res)
          this.$router.push('/login', this.$router.go(0))
        })
        .catch((error) => {
          console.log(error)
        })
    },
    updateProfile() {
      const updateUser = {
        firstName: this.firstName || this.user.firstName,
        lastName: this.lastName || this.user.lastName,
        password: this.password || this.user.password,
        campus: this.campus || this.user.campus
      }
      Api.patch(`/users/${this.user.id}`, updateUser).then(
        (res) => {
          console.log(res)
          this.$router.push('/')
        },
        (err) => {
          console.log(err.response)
        }
      )
    }
  }
}
</script>
